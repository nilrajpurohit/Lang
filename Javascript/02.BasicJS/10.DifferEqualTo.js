// === vs ==
// == is just check the value.
// but === check the value as well as data type.

//Example..

if(2 == "2"){
    console.log("Both are equal");
}else{
    console.log("Both are not equal");
}


if(2 === "2"){
    console.log("Both are equal");
}else{
    console.log("Both are not equal");
}



if(true == 1){
    console.log("Both are equal");
}else{
    console.log("Both are not equal");
}


if(true === 1){
    console.log("Both are equal");
}else{
    console.log("Both are not equal");
}